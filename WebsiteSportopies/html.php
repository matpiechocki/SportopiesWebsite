	Dogtrekking 
	<img class ="symbole" src="symbol.png" alt="symbol">
	</h2>
	<p class="tresc">Warto na początek wprowadzić trekking raz w tygodniu, a z czasem rozszerzyć nawet do trzech razy w tygodniu.

Stopniowo też zmieniają trasy trekkingu z psem. Zacznijcie od płaskich terenów. Zdecydowanie wybierajcie ścieżki bez asfaltu! Omijajcie także chodniki. Lepiej będzie spacerować po lesie czy łąkach
    </p>
    <p class="tresc">Dogtrekking to sport, który nie wymaga wielkich nakładów finansowych, specjalistycznego sprzętu czy wiedzy. Podstawą są odpowiednio dobrane szelki np typu sportowego-sled po guardy. Kolejnym istotnym elementem jest pas biodrowy
	</p>
