	<img class ="symbole" src="symbol.png" alt="symbol">
	Psie zaprzęgi
	<img class ="symbole" src="symbol.png" alt="symbol">
	</h2>
	<p class="tresc">Psie zaprzęgi dzielą się na wiele rodzi. Wiele zależy od wyboru wózka zaprzęgowego. Dzielą się one na hulanogi, dwukołowce oraz czterokołowce.
	</p>
	<p class="tresc">Odróżniamy też Skijoring czyli  maszer, będący przyczepiony do psa lub psów linką z amortyzatorem, porusza się na nartach biegowych
    </p>
