	<h2 class="tytul">
	<img class ="symbole" src="symbol.png" alt="symbol">
	Canicross-Jaki sprzęt wybrać? Jak przygotować siebie i psa?
	<img class ="symbole" src="symbol.png" alt="symbol">
	</h2>
	<p class="tresc">Przydadzą się umiejętności takie jak: ciągła chęć biegania, startowanie na odliczanie, skręcanie na komendy, szybkie biegniecie do przodu. Ważne jest aby zacząć treningi od małych dystansów np. 1km i zaczynać bieg ustaloną komendą wprowadzając psa tym samym w szybki bieg utrzymujemy jego tempo i nagradzamy napiętą smycz oraz poprawne skręcenie.
	</p>
	<p class="tresc">
	Do canicrossu potrzebujemy wygodne szelki dla psa, w których pies będzie mógł ciągnąć bez pododuszania się. 
	Dobrym wyborem są szelki zaprzęgowe typu sled lub sportowe szelki o przedłużonym tyle. Odradza się szelek w typie norweskim ponieważ blokuje on naturalny ruch łopatek. 
	</p>
	<img class="galery" src="o6.jpg" alt="obraz6" width="313" height="520">
	<img class="galery" src="o7.jpg" alt="obraz7" width="313" height="520">
	<p class="tresc"> Pas maszera pozwala nam zaczepić psa bezpośrednio do na, dzięki czemu mamy wolne ręce by podczas biegu pracować nimi.  Należy zwrócić szczególną uwagę na dobór pasa odpowiedniego dla nas. Psy wolniejsze, o mniejszej masie mogą być zaczepione do pasów szerszych, które nosimy wyżej na lędźwiach. Szybkie, silne i mocne psy będą powodowały zbyt duże obciążenia na nasz kręgosłup. Wówczas wybierzmy pas gdzie środek uciągu będzie niżej np;. na kościach kulszowych, tak by pies ciągną bardziej za biodra niż za krzyż. 
	</p>
	<img class="galery" src="o8.jpg" alt="obraz8" width="800" height="467">
	<p class="tresc"> Ostatnim elementem jest smycz z amortyzatorem, lub amortyzator + smycz, które pozwalają zminimalizować szarpnięcia zarówno samego psa jak i te odczuwalne na naszym kręgosłupie.
	</p>
