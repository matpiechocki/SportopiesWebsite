	<h2 class="tytul">
	<img class ="symbole" src="symbol.png" alt="symbol">
	Bikejorng
	<img class ="symbole" src="symbol.png" alt="symbol">
	</h2>
	<p class="tresc">Rowery do bikejoringu to zazwyczaj rowery MTB (popularnie nazywane “górskimi”) hardtail (z angielskiego”sztywny ogon” czyli bez tylnego amortyzatora). Musi on być wyposażony w niezależne hamulce na przód i tył oraz posiadać wysięgnik do bikejoringu.
	</p>
	<p class="tresc"> Nauka komend typu “prawo”, “lewo” czy “stój” to absolutne podstawy do tego, by móc bezpiecznie i sprawnie pokonywać wyznaczone trasy.
W dalszym etapie szkolenia pies powinien być również przyzwyczajany do bezproblemowego wyprzedzania innych teamów, z którymi będziemy konkurować na zawodach.
Psiego sportowca do wysiłku należy przygotowywać stopniowo i najlepiej pod okiem doświadczonego zawodnika, który pomoże nam ustalić odpowiedni plan treningowy i da niezbędne wskazówki.
	</p>
	<p class="tresc">Najczęściej zawodnicy dla swoich czworonożnych sportowców, wybierają szelki typu sled. 
	</p>
